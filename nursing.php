i.  Click on Admission;<br /> 
ii. Click on Postgraduate School;<br />
iii. Candiadates will be directed to  <a href="https://www.spgs.unimed.edu.ng/"  target="_blank">https://www.spgs.unimed.edu.ng/</a> <br />
