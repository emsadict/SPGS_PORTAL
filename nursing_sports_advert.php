ADMISSION REQUIREMENTS FOR THE PROGRAMMES
Entry Requirements for M.Sc Programme.
Graduates of the University of Medical Sciences or other recognized Universities who have obtained the degree of Bachelor in Nursing (BNSc) with at least second class lower honours or it's equivalent e.g. PGDN or PGDNE. Candidate must be registered with the Nursing and Midwifery Council of Nigeria (N&MCN) as a registered Nurse and have a current licence to practice in Nigeria. Other selection considerations include Grade Point Average of not less than 2.4 on a 5 point scale or its equivalent. Applicant's submission of all admission requirements as in PG school admission guideline is essential.
Entry Requirements for MPhil/Ph.D Programme
A candidate with Master's degree with CGPA of 3.5 on a 5-point scale from a related Nursing Department/discipline who wishes to do a Ph.D work in Nursing, he/she must be admitted for MPhil/Ph.D degree programme. Candidates must also have five credit passes in English, Mathematics, Biology, Chemistry and Physics at O'Level.
Candidates must have Bachelor's Degree in Nursing from an approved University with CGPA of 3.5 on a 5 point scale.
Candidates with Professional Master's in Nursing with a GPA of 2.4 on a 5-point scale or weighted average of 55-59.99% are also eligible for admission into MPhil/Ph.D programme.
Entry Requirements for Ph.D Programme
Candidates for Ph.D admission must satisfy the following condition:
All candidates for Ph.D admission must have five credit passes in English, Mathematics, Chemistry, Physics and Biology at G.C.E 'O'Level/SSCE.
Candidates with Bachelor's degree in nursing from a recognized University must obtain a minimum of Second Class Lower Division with a CGPA of not less than 2.4 on a 5 point scale or its equivalent. Graduates of the University of Medical Sciences or of other recognized Universities who have obtained the degree of Master's in Nursing, with a minimum of 4.00 Grade Point Average on 5 point(or at least 60% weighted average) are eligible provided that satisfactory research work formed part of the degree programme. The dissertation score should not be lower than 60% (B). 
Candidates must demonstrate adequate intellectual capacity, maturity and effective decision making and problem solving potentials.
           

					
