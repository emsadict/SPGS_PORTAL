          <li>Click <strong>Payment</strong> and choose your category (Doctorate / Masters / Postgraduate Diploma)</li>
          <li><strong>Note:</strong> Use <u>MasterCard</u> or <u>Visa</u> card only. <strong>Verve is not accepted.</strong></li>
          <li>After successful payment, you will be redirected to the Application Form.</li>
          <li>Print your application receipt and use the details provided (REG No. and TRANSACTION ID) to access your application form.</li>
