        <h4><strong>FACULTY OF HEALTH LAW AND HUMANITIES</strong></h4>

        <h5><strong>A. DEPARTMENT OF HEALTH LAW POLICY AND MANAGEMENT</strong></h5>
        <p class="description">
          <strong>(i) MSc/MPhil/PhD in Health Law and Policy</strong><br />
          The programme is designed to:
          <ul>
            <li>Enhance competencies of health professionals, service managers, and legal practitioners in Health Law and Policy.</li>
            <li>Promote international best practices in national and global Health Law.</li>
            <li>Encourage research and innovation in Health Law and Policy within Nigerian academia.</li>
          </ul>
          Graduates will:
          <ul>
            <li>Understand contemporary issues and advances in Health Law and Policy.</li>
            <li>Apply knowledge to health policy formulation and service delivery.</li>
            <li>Support legal frameworks for clinical practice.</li>
          </ul>
          <strong>Programme Structure:</strong><br />
          • Duration: One calendar year<br />
          • Two semesters of coursework + 3-month dissertation<br />
          • Hybrid delivery: virtual + face-to-face (1 week/semester)<br />
          • Two seminar presentations required
        </p>

        <p class="description">
          <strong>Admission Requirements:</strong><br />
          • MSc: First degree in Medicine, Dentistry, Law, or related Health/Social Sciences (MBBS, BDS, LLB, BSc 2:2 or higher)<br />
          • MPhil/MPhil-PhD: MSc in Health Law and Policy with 50–59.9% average<br />
          • PhD: MSc with dissertation in Health Law/related field and minimum 60% average
        </p>

        <p class="description">
          <strong>(ii) Executive MBA in Health Management</strong><br />
          Designed to develop skilled managers for hospital and healthcare administration. Objectives include:
          <ul>
            <li>Enhancing performance and responsibility in dynamic health environments</li>
            <li>Training in problem-solving for public, private, and global health organizations</li>
            <li>Instilling ethics, ICT skills, and entrepreneurial leadership</li>
          </ul>
          <strong>Programme Structure:</strong><br />
          • Duration: Two calendar years<br />
          • Four semesters of online coursework + 4-month dissertation<br />
          • Seminar presentations in third semester
        </p>

        <p class="description">
          <strong>Admission Requirements:</strong><br />
          • Five O’Level credits including English and Mathematics<br />
          • Bachelor’s degree from approved university<br />
          • Minimum one year managerial/administrative experience<br />
          • HND holders with relevant experience may be considered
        </p>

        <h5><strong>B. DEPARTMENT OF HUMANITIES, GENERAL STUDIES AND BIOETHICS</strong></h5>
        <p class="description">
          <strong>PgD/MSc/MPhil/PhD in Healthcare Entrepreneurship and Innovation Management</strong><br />
          Programme goals:
          <ul>
            <li>Develop entrepreneurial mindsets for innovation in healthcare</li>
            <li>Enable viable healthcare start-ups and job creation</li>
            <li>Promote creativity and innovation management in the health ecosystem</li>
          </ul>
          <strong>Programme Structure:</strong><br />
          • Duration: One calendar year (PgD/MSc)<br />
          • Two semesters of coursework + 3-month project/dissertation<br />
          • Hybrid delivery: virtual + face-to-face (1 week/semester)<br />
          • PgD: 16-week Industrial Attachment<br />
          • MSc/PhD: Two seminar presentations
        </p>

        <p class="description">
          <strong>Admission Requirements:</strong><br />
          • PgD: HND or BSc/B.Tech with Third-Class in health-related discipline<br />
          • MSc: Second-Class Lower in health-related discipline or PgD with Upper Credit<br />
          • MPhil/MPhil-PhD: MSc in Healthcare Entrepreneurship with 50–59.9% average<br />
          • PhD: MSc with dissertation or M.Eng/M.Tech/M.Sc in related field with 60%+ or 3.50 CGPA
        </p>
